CREATE TABLE if not exists reg_rkk_mailing
(
    id     integer generated by default as identity not null
        constraint reg_rkk_mailing_pk
            primary key,
    id_rkk             integer
        constraint fk_doc_rkk
            references doc_rkk,
    id_organization    integer
        constraint fk_organization
            references cls_organization,
    note   text,
    date   date
);

CREATE TABLE if not exists cls_rkk_stage
(
    id     integer generated by default as identity not null
        constraint cls_rkk_stage_pk
            primary key,
    name   text,
    code   varchar(16)
);

CREATE TABLE if not exists reg_rkk_visa
(
    id     integer generated by default as identity not null
        constraint reg_rkk_visa_pk
            primary key,
    id_rkk             integer
        constraint fk_doc_rkk
            references doc_rkk,
    id_stage  integer
        constraint fk_cls_rkk_stage
            references cls_rkk_stage,
    note   text,
    date   date
);